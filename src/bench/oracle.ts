// src/bench/oracle.ts
// Phase 3 rebuild: content-based ground-truth oracle.
//
// REPLACES label-derived ground truth. The previous proxy (isSlop = wontfix|invalid|stale|...)
// reflected MAINTAINER PROCESS (who has time to triage what), not ISSUE QUALITY (does the body
// contain enough information to act). Evidence from fixture sampling:
//   - rust 156060: D-invalid-suggestion (a rust diagnostics taxonomy label) → proxy=slop, but
//     the body is a top-quality bug report with rustc trace, repro code, Rust Version section.
//   - react 34884: Resolution: Stale → proxy=slop, but body has Steps To Reproduce, expected/
//     actual headings, version, repro URL.
//   - rust 43535: no slop labels → proxy=actionable, but body is 2 sentences of speculation
//     ("Wild stab in the dark") with no repro, no version, no code.
//
// The oracle scores quality from the body STRUCTURE — what a maintainer would actually see —
// not from labels. It strips HTML comments and auto-generated <details> boilerplate, then
// counts 7 binary content signals. Quality = sum (0..7). isSlop = quality < OracleConfig.slopMax.
//
// NOT a duplicate of src/core/heuristics/extractor.ts:
//   - Oracle deliberately uses signals the production extractor does NOT: substantive prose
//     length, repro URL detection, ordered list with non-trivial content per item.
//   - This non-overlap is what makes the oracle a useful independent reference. If both used
//     identical signals, agreement (Cohen's κ) would be trivially perfect.

import type { Code, Heading, List, ListItem, Paragraph, Root, Text } from 'mdast'
import { toString as mdastToString } from 'mdast-util-to-string'
import remarkParse from 'remark-parse'
import { unified } from 'unified'
import { visit } from 'unist-util-visit'

export interface OracleResult {
  quality: number // 0..7 — count of binary content signals present
  isSlop: boolean // quality < slopThreshold (default 3)
  reasons: string[] // each fired signal name (for transparency in REPORT.md)
  userBodyLen: number // length AFTER stripping HTML comments and auto-generated <details>
  totalLen: number // raw body length (for evidence)
}

export interface OracleConfig {
  /** Quality strictly below this counts as slop. Default 3 (i.e., needs >=3 of 7 signals). */
  slopThreshold: number
}

const DEFAULT_CONFIG: OracleConfig = { slopThreshold: 3 }

// ---------------------------------------------------------------------------
// Boilerplate stripping
// ---------------------------------------------------------------------------

const HTML_COMMENT = /<!--[\s\S]*?-->/g

// <details><summary>X</summary>...</details> where X matches an auto-generated header
// known across the three benchmark repos. These blocks are noise — they're injected by
// the bug-reporter extension or template, not written by the user.
const AUTOGEN_DETAILS_SUMMARIES = [
  /system\s*info/i,
  /a\/b\s*experiments?/i,
  /process\s*info/i,
  /workspace\s*info/i,
  /extensions?\s*\(?\d/i, // "Extensions (25)"
  /traceback/i,
  /backtrace/i,
  /environment/i,
]

function stripAutoGeneratedDetails(body: string): string {
  // Match <details>...</details> blocks and drop those whose <summary> matches.
  return body.replace(/<details[\s\S]*?<\/details>/gi, (block) => {
    const summaryMatch = block.match(/<summary>([\s\S]*?)<\/summary>/i)
    const summary = summaryMatch?.[1] ?? ''
    if (AUTOGEN_DETAILS_SUMMARIES.some((re) => re.test(summary))) return ''
    return block
  })
}

// vscode bug-reporter injects "<!-- generated by issue reporter -->" and rust triagebot
// injects "<!-- TRIAGEBOT_*_START --><!-- TRIAGEBOT_*_END -->". HTML comments are always noise.
function stripBoilerplate(body: string): string {
  return stripAutoGeneratedDetails(body.replace(HTML_COMMENT, ''))
}

// ---------------------------------------------------------------------------
// Content signal detectors
// ---------------------------------------------------------------------------

const SEMVER = /\bv?\d+\.\d+\.\d+\b/
const RUNTIME_VERSION =
  /\b(node|npm|python|ruby|rust|rustc|cargo|go|java|gradle|maven|dotnet)\s+v?\d/i
const PLATFORM_VERSION =
  /\b(macos|darwin|windows|ubuntu|debian|fedora|linux|ios|android)\s*[\w.-]*\s*\d/i
// rust-style "1.94" / "1.95" or library "v18" — only count when in a version context,
// since bare "X.Y" matches dates, fractions, etc. The context word must be on the same line.
const VERSION_IN_CONTEXT =
  /\b(version|regression|release|stable|nightly|beta|since|using|installed|with|on)\s+\S*v?\d+\.\d+\b/i

const STACK_FRAME_PATTERNS = [
  /^\s+at\s+\S+/m, // JS/Java: "    at fn (file:1:1)"
  /^\s*File\s+"[^"]+",\s+line\s+\d+/m, // Python: 'File "x.py", line 3'
  /^\s*Traceback\s*\(/m, // Python traceback header
  /\berror\[E\d{2,5}\]/, // Rust diagnostic code
  /^\s*panicked\s+at/m, // Rust panic
  /^\s*\d+:\s+0x[0-9a-f]+\s+-/m, // Rust backtrace frame
  /^Exception\s+in\s+thread/m, // Java/Kotlin exception
  /^\s*Caused\s+by:/m, // JVM cause chain
]

function countStackFrames(text: string): number {
  let n = 0
  for (const re of STACK_FRAME_PATTERNS) {
    const all = text.match(
      new RegExp(re.source, re.flags.includes('g') ? re.flags : `${re.flags}g`),
    )
    if (all) n += all.length
  }
  // Also count multi-line "at " frames specifically (most common case for stacks)
  const atFrames = text.match(/^\s+at\s+\S/gm)
  if (atFrames && atFrames.length >= 2) n = Math.max(n, atFrames.length)
  return n
}

// "Repro URL" — links to playgrounds, sandboxes, or specific git branches/lines.
// Strong signal that the reporter staged a minimal reproduction.
// Dedicated repro hosts (playgrounds, sandboxes) — strong signals.
const REPRO_HOST_PATTERNS = [
  /codesandbox\.io/i,
  /codepen\.io/i,
  /stackblitz\.com/i,
  /jsfiddle\.net/i,
  /replit\.com/i,
  /play\.rust-lang\.org/i,
  /godbolt\.org/i,
  /playground\.babylonjs\.com/i,
  /gist\.github\.com/i,
]

// github.com links: count tree/blob/line links AND repos-as-repros (e.g.
// github.com/gaearon/react-udv-bug/), but NOT cross-references to issues / PRs /
// discussions in another repo (those are not minimal reproductions).
const GITHUB_REPRO_REPO =
  /https?:\/\/github\.com\/[\w.-]+\/[\w.-]+\/?(?!\/?(?:issues|pull|pulls|discussions|releases|wiki|actions|projects)\b)/i
const GITHUB_TREE_OR_LINE =
  /github\.com\/[^/\s]+\/[^/\s]+\/(?:tree|blob|commit)\/|github\.com\/[^/\s]+\/[^/\s]+\/.+#L\d+/i

function hasReproUrl(text: string): boolean {
  if (REPRO_HOST_PATTERNS.some((re) => re.test(text))) return true
  if (GITHUB_TREE_OR_LINE.test(text)) return true
  if (GITHUB_REPRO_REPO.test(text)) return true
  return false
}

// "Expected/actual structure" — three accepted forms, each more permissive than the next:
//   (a) two distinct headings, one containing "expected" the other "actual" (or "current")
//   (b) two list items / paragraphs labeled "Expected:" and "Actual:" inline
//   (c) "## Expected Behavior" + "## Current Behavior" pattern (react templates use Current)
function detectExpectedActual(headingTexts: string[], plainText: string): boolean {
  const hasExpectedHeading = headingTexts.some((h) => /expected/.test(h))
  const hasActualHeading = headingTexts.some((h) => /(actual|current)\b/.test(h))
  if (hasExpectedHeading && hasActualHeading) return true

  // Labeled paragraph form: "Expected:" / "Actual:"
  const labeledExpected =
    /(^|\n)\s*(\*\*)?expected\s*(behavior|behaviour|result)?\s*(\*\*)?\s*[:：]/i.test(plainText)
  const labeledActual =
    /(^|\n)\s*(\*\*)?(actual|current)\s*(behavior|behaviour|result)?\s*(\*\*)?\s*[:：]/i.test(
      plainText,
    )
  if (labeledExpected && labeledActual) return true

  // Prose form: "I expected X / Instead, Y" or "expected: X / instead: Y"
  const proseExpected = /\bI\s+expected\b|\bexpected\s+(to|that|behavior|result)\b/i.test(plainText)
  const proseInstead =
    /\binstead[,:]?\s+(this\s+happened|I\s+(got|see|saw)|the\b)|\bbut\s+(instead|actually|got)\b|\bbut\s+I\s+(got|see|saw)\b|\bhowever\b.*\b(instead|actually)\b/i.test(
      plainText,
    )
  return proseExpected && proseInstead
}

// "Substantive prose" — after stripping code blocks and boilerplate, the remaining text
// has enough words to convey a real description. Catches one-liner ("doesn't work") and
// rant-with-template-noise issues (vscode 304089).
// Plain "Steps to reproduce:" / "Reproduction:" / "To reproduce:" followed by >=3 short
// non-empty lines within the next ~400 chars. Handles bug-reporter output and casual writers.
function detectPlainStepList(body: string): boolean {
  const match = body.match(
    /(?:^|\n)\s*(?:steps?\s+to\s+reproduce|reproduction\s+steps?|to\s+reproduce|how\s+to\s+reproduce)\s*[:：]\s*\n/i,
  )
  if (!match) return false
  const start = (match.index ?? 0) + match[0].length
  const window = body.slice(start, start + 400)
  // Count non-empty lines until we hit a blank line or markdown heading
  const lines = window.split('\n')
  let count = 0
  for (const line of lines) {
    const trimmed = line.trim()
    if (trimmed === '') break
    if (/^#{1,6}\s/.test(trimmed)) break // next markdown heading ends the list
    if (/^<\w/.test(trimmed)) break // html block (e.g. <details>)
    if (trimmed.length < 3) continue
    count++
    if (count >= 3) return true
  }
  return count >= 3
}

function hasSubstantiveProse(plainText: string): boolean {
  // Count words >= 3 chars (filters "is/to/of/the" noise loosely).
  const words = plainText.split(/\s+/).filter((w) => w.replace(/[^a-zA-Z]/g, '').length >= 3)
  return words.length >= 25 && plainText.length >= 120
}

// ---------------------------------------------------------------------------
// Main oracle
// ---------------------------------------------------------------------------

export interface OracleInput {
  title: string
  body: string
}

export function oracle(issue: OracleInput, config: OracleConfig = DEFAULT_CONFIG): OracleResult {
  const totalLen = (issue.body ?? '').length
  const cleanedBody = stripBoilerplate(issue.body ?? '')
  const userBodyLen = cleanedBody.length

  const tree = unified().use(remarkParse).parse(cleanedBody) as Root

  // Collect AST nodes of interest from CLEANED tree
  const codeNodes: Code[] = []
  const orderedLists: List[] = []
  const headingTexts: string[] = []
  let textConcat = ''

  visit(tree, 'code', (n: Code) => {
    codeNodes.push(n)
  })
  visit(tree, 'list', (n: List) => {
    if (n.ordered) orderedLists.push(n)
  })
  visit(tree, 'heading', (n: Heading) => {
    headingTexts.push(mdastToString(n).toLowerCase().trim())
  })
  visit(tree, 'text', (n: Text) => {
    textConcat += ` ${n.value}`
  })
  visit(tree, 'paragraph', (n: Paragraph) => {
    textConcat += ` ${mdastToString(n)}`
  })

  // Plain text without code-block content, for prose-length judgment
  const plainText = textConcat.trim()
  const codeText = codeNodes.map((n) => n.value ?? '').join('\n')

  const reasons: string[] = []

  // 1. Substantive prose (after stripping boilerplate and code)
  const sigSubstantive = hasSubstantiveProse(plainText)
  if (sigSubstantive) reasons.push('substantive-prose')

  // 2. Repro steps — accepted forms:
  //   (a) markdown ordered list with >=2 substantive items
  //   (b) plain "Steps to reproduce:" followed by >=3 non-empty lines (handles bug-reporter
  //       output and casual writers who don't use markdown lists)
  const sigOrderedList = orderedLists.some((list) => {
    const items = (list.children ?? []) as ListItem[]
    if (items.length < 2) return false
    const substantiveItems = items.filter((it) => mdastToString(it).trim().length >= 10)
    return substantiveItems.length >= 2
  })
  const sigPlainSteps = detectPlainStepList(cleanedBody)
  const sigReproList = sigOrderedList || sigPlainSteps
  if (sigReproList) reasons.push('repro-list')

  // 3. Code block with real content (not just `<placeholder>`)
  const sigCodeBlock = codeNodes.some((n) => {
    const content = (n.value ?? '').trim()
    if (content.length < 20) return false
    if (/^<[^>]+>$/.test(content)) return false // single placeholder token
    if (/^(<[^>]+>\s*){1,3}$/.test(content)) return false // only placeholders
    return true
  })
  if (sigCodeBlock) reasons.push('code-block')

  // 4. Multi-frame stack trace or explicit error pattern
  const stackFrames = countStackFrames(codeText) + countStackFrames(plainText)
  const sigStackTrace = stackFrames >= 2
  if (sigStackTrace) reasons.push('stack-trace')

  // 5. Version / runtime info
  const sigVersion =
    SEMVER.test(plainText) ||
    SEMVER.test(codeText) ||
    RUNTIME_VERSION.test(plainText) ||
    RUNTIME_VERSION.test(codeText) ||
    PLATFORM_VERSION.test(plainText) ||
    VERSION_IN_CONTEXT.test(plainText) ||
    VERSION_IN_CONTEXT.test(codeText)
  if (sigVersion) reasons.push('version')

  // 6. Expected / actual structure (headings or labeled paragraphs)
  const sigExpectedActual = detectExpectedActual(headingTexts, plainText)
  if (sigExpectedActual) reasons.push('expected-actual')

  // 7. Repro URL (sandbox / playground / explicit git tree or line link)
  const sigReproUrl = hasReproUrl(cleanedBody)
  if (sigReproUrl) reasons.push('repro-url')

  const quality = reasons.length
  const isSlop = quality < config.slopThreshold

  return { quality, isSlop, reasons, userBodyLen, totalLen }
}
